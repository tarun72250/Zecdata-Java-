package day03;

public class A_10_TernaryOpertor {

	public static void main(String[] args) {
		
		
		int a= 10;
		int b= 20;
		
		boolean result = (a<b)?true:false;
		System.out.println(result);
		
		String result1 = (a>b)?"Apple":"banana";
		System.out.println(result1);
		
		
		int z = (a>b)?1:2;
		System.out.println(z);
		
		
	}
}
