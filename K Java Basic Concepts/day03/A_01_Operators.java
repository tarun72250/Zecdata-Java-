package day03;

public class A_01_Operators {

	public static void main(String[] args) {
		
		
		//unary operator
		
		//PostPrefix or post increment
		int a = 10 ;
		System.out.println(a);//10
		System.out.println(a++);//10
		System.out.println(a);//11
		System.out.println(a--);//11
		System.out.println(a);//10
		
		//prefix or pre decrement
		System.out.println(--a);//9
		System.out.println(a);//9
		System.out.println(++a);//10
		
		
		

	}
}
