package day03;

public class A_02_Addition {

	public static void main(String[] args) {
		
		int a =  10;
		int b =  20;
		System.out.println(a+b);
		
		int result = a+b;
		System.out.println(result);
		
		int c = 10;
		
		int d = 10;
		c += d; // c = 10 + 10
		System.out.println(c);
		
		
		int f = 250;
		f += 250;//f = 250 + 250;
		System.out.println("value of f ="+f);//500
		
		int g = 100;
		g -= -800;//900
		System.out.println(g);
	}
}
